$(document).ready(function() {
    var csv_file = document.querySelector("#csv_file");
    var filename =  csv_file.textContent;
    console.log("Filename: ", filename);

    var TITLE = 'Food Statistical Chart';

    // `false` for vertical column chart, `true` for horizontal bar chart
    var HORIZONTAL = false;

    // `false` for individual bars, `true` for stacked bars
    var STACKED = true;  
    
    // Which column defines 'bucket' names?
    var LABELS = 'names';  

    // For each column representing a data series, define its name and color
    var SERIES = [  
        {
        column: 'calories',
        name: 'fat',
        color: 'red'
        },
        {
        column: 'protein',
        name: 'protein',
        color: 'green'
        },
        {
        column: 'fat',
        name: 'fat',
        color: 'blue'
        },
        {
        column: 'carbs',
        name: 'carbs',
        color: 'yellow'
        },
        {
        column: 'fiber',
        name: 'fiber',
        color: 'purple'
        }
    ];

    // x-axis label and label in tooltip
    var X_AXIS = 'Elements Info';

    // y-axis label, label in tooltip
    var Y_AXIS = 'Amount';

    // `true` to show the grid, `false` to hide
    var SHOW_GRID = true; 

    // `true` to show the legend, `false` to hide
    var SHOW_LEGEND = true; 

    // Read data file with random string generated by current time
    // to bypass browser cache, and create chart
    $.get(filename, {'_': $.now()}, function(csvString) {

        var rows = Papa.parse(csvString, {header: true}).data;

        var datasets = SERIES.map(function(el) {
        return {
            label: el.name,
            labelDirty: el.column,
            backgroundColor: el.color,
            data: []
        }
        });

        rows.map(function(row) {
        datasets.map(function(d) {
            d.data.push(row[d.labelDirty])
        })
        });

            var barChartData = {
        labels: rows.map(function(el) { return el[LABELS] }),
                datasets: datasets
        };

        var ctx = document.getElementById('container').getContext('2d');

        new Chart(ctx, {
        type: HORIZONTAL ? 'horizontalBar' : 'bar',
        data: barChartData,
        
        options: {
            title: {
            display: true,
            text: TITLE,
            fontSize: 14,
            },
            legend: {
            display: SHOW_LEGEND,
            },
            scales: {
            xAxes: [{
                stacked: STACKED,
                scaleLabel: {
                display: X_AXIS !== '',
                labelString: X_AXIS
                },
                gridLines: {
                display: SHOW_GRID,
                },
                ticks: {
                beginAtZero: true,
                callback: function(value, index, values) {
                    return value.toLocaleString();
                }
                }
            }],
            yAxes: [{
                stacked: STACKED,
                beginAtZero: true,
                scaleLabel: {
                display: Y_AXIS !== '',
                labelString: Y_AXIS
                },
                gridLines: {
                display: SHOW_GRID,
                },
                ticks: {
                beginAtZero: true,
                callback: function(value, index, values) {
                    return value.toLocaleString()
                }
                }
            }]
            },
            tooltips: {
            displayColors: false,
            callbacks: {
                label: function(tooltipItem, all) {
                return all.datasets[tooltipItem.datasetIndex].label
                    + ': ' + tooltipItem.yLabel.toLocaleString();
                }
            }
            }
        }
        });

    });

});
